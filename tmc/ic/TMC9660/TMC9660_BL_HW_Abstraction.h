#ifndef TMC_REGISTERFIELD32_DEFINED
#define TMC_REGISTERFIELD32_DEFINED
typedef struct
{
    uint32_t mask;
    uint8_t shift;
    uint32_t address;
    bool isSigned;
} RegisterField32;


static inline uint32_t field_extract32(uint32_t data, RegisterField32 field)
{
    uint32_t value = (data & field.mask) >> field.shift;

    if (field.isSigned)
    {
        // Apply signedness conversion
        uint32_t baseMask = field.mask >> field.shift;
        uint32_t signMask = baseMask & (~baseMask >> 1);
        value = (value ^ signMask) - signMask;
    }

    return value;
}

static inline uint32_t field_update32(uint32_t data, RegisterField32 field, uint32_t value)
{
    return (data & (~field.mask)) | ((value << field.shift) & field.mask);
}

#endif /* TMC_REGISTERFIELD32_DEFINED */

#ifndef TMC_REGISTERFIELD16_DEFINED
#define TMC_REGISTERFIELD16_DEFINED
typedef struct
{
    uint16_t mask;
    uint8_t shift;
    uint32_t address;
    bool isSigned;
} RegisterField16;

static inline uint16_t field_extract16(uint16_t data, RegisterField16 field)
{
    uint16_t value = (data & field.mask) >> field.shift;

    if (field.isSigned)
    {
        // Apply signedness conversion
        uint16_t baseMask = field.mask >> field.shift;
        uint16_t signMask = baseMask & (~baseMask >> 1);
        value = (value ^ signMask) - signMask;
    }

    return value;
}

static inline uint16_t field_update16(uint16_t data, RegisterField16 field, uint16_t value)
{
    return (data & (~field.mask)) | ((value << field.shift) & field.mask);
}
#endif /* TMC_REGISTERFIELD16_DEFINED */

